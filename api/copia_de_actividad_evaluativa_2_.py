def cramers_v(col, target):
    tabla = pd.crosstab(col.astype(str), target.astype(str))
    if tabla.shape[0] < 2 or tabla.shape[1] < 2:
        return 0.0
    chi2  = chi2_contingency(tabla, correction=False)[0]
    n     = tabla.sum().sum()
    r, k  = tabla.shape
    denom = n * (min(r, k) - 1)
    return np.sqrt(chi2 / denom) if denom > 0 else 0.0

