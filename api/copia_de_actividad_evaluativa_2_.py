def create_objective(model_name, base_model, param_space):
    def objective_func(trial):
        # 1. Clonar el modelo REALMENTE para asegurar que cada trial comience limpio
        # Esto evita problemas de referencia de memoria en Python
        current_model = clone(base_model)

        # 2. Sugerir hiperparámetros para el modelo específico usando Optuna
        model_params = {}
        for param_name, suggest_func in param_space.items():
            suggested_value = suggest_func(trial)
            if suggested_value is not None:
                model_params[param_name] = suggested_value

        # 3. Inyectar los hiperparámetros sugeridos al modelo clonado
        current_model.set_params(**model_params)

        # 4. Evaluar utilizando validación cruzada estratificada
        cv = StratifiedKFold(n_splits=5, shuffle=True, random_state=42)

